		uint32 FindResID(std::string ResName)
		{
			for (auto i : ResNames)
			{
				if (i.second == ResName)
					return i.first;
			}

			return 0;
		}

		std::u16string ToU16(std::string _string)
		{
			std::wstring_convert<std::codecvt_utf8_utf16<char16_t>, char16_t> convert;
			return convert.from_bytes(_string);
		}

		std::string ToU8(std::u16string _string16)
		{
			std::wstring_convert<std::codecvt_utf8_utf16<char16_t>, char16_t> convert;
			return convert.to_bytes(_string16);
		}
